		Nature's Influence: Mother and father are both perceived as very agreeable, friendly people <br>
		Nurture's influence: Past experiences and treatment from others <br>
		Analysis of trait: Agreeableness is also one of the 5 Big Personality traits that reflects cooperation and social harmony. 
		Similar to the neuroticism analysis, I took the online version of the 5 Big Personality Traits test
		to determine how "agreeable" of a person I am. With this online test, I scored around 80% (0%-least agreeable; 100% most agreeable).
		My parents also took the test; my mother scored 85%, and my father scored 90%. <i>(note: As I have mentioned in the previous section for
		neuroticism, I am not taking these numbers at face value. However, these scores give me a general sense of how my parents and I compare to 
		other individuals.)</i> Agreeableness and conscientiousness have a slighly lower heritability index compared to the other three behavioral traits.
		This may primarily be because these two traits (agreeableness and conscientiousness) affect the overall likability of a person. For the human
		species, the "likability" score often affects the social "fitness" of an individual; more likable humans are more likely to succeed. 
		Hence, people learn instantaneously (thanks to our large, developed brains) to adapt to become more agreeable and conscientious people.
		<a href="https://www.ncbi.nlm.nih.gov/pmc/articles/PMC2630227/">Studies</a> have also found that agreeableness has a higher mean
		score in the Asian population compared to the American/European population. Some hypothesize that this could be due to the fact that
		Asian culture generally tends to value collectivism more than European cultures, where indvidualism is more valued. It is truly
		fascinating to learn about how Nature and Nurture work together to shape these behavioral traits.
