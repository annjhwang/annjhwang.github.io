		worry, envy, frustration, jealousy, and loneliness. I had my two parents take the test as well; my mother got a score of 52%, while my
		father received a score of 62%. THough it is clear that genetics and nature has a great influence on one's neurotic score, it is also
		important to realize that nurture/environment has an equally important role as well. <a href="https://www.ncbi.nlm.nih.gov/pmc/articles/PMC2630227/">
